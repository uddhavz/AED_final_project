    private void btnModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblCounty.getSelectedRow();
        if (selectedRow < 0) {
            CallDialog callDialog = new CallDialog(mainPanel, "Please select a row to modify", false);
            btnModify.setEnabled(true);
            return;
        }

        btnAdd.setVisible(false);
        btnDelete.setEnabled(false);
        btnModify.setEnabled(false);
        btnSubmit.setVisible(true);

        DefaultTableModel model = (DefaultTableModel) tblCounty.getModel();
        County county = (County)model.getValueAt(selectedRow, 0);

        txtCountyName.setText(county.getCounty());
        this.county = county;
    }//GEN-LAST:event_btnModifyActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblCounty.getSelectedRow();
        if (selectedRow < 0) {
            CallDialog callDialog = new CallDialog(mainPanel, "Please select a row to delete", false);
            return;
        }

        County county = (County) tblCounty.getValueAt(selectedRow, 0);

        this.enterprise.getCountyDirectory().deleteCounty(county);

        populateCountyTable();
        CallDialog callDialog = new CallDialog(mainPanel, "Manufacturing county deleted successfully", true);
    }//GEN-LAST:event_btnDeleteActionPerformed

