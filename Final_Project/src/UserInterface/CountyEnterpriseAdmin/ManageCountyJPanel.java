    private void populateCountyTable() {

        DefaultTableModel model = (DefaultTableModel) tblCounty.getModel();
        model.setRowCount(0);

        for (County county : enterprise.getCountyDirectory().getCountyDirectoryArray()) {
            Object[] row = new Object[1];
            row[0] = county;
            model.addRow(row);
        }
    }

