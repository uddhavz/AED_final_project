    private boolean isValidData() {
        
        boolean isValid = true;
        
        if (txtTest.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblTestValidation, "Please enter test name");
            
        }
        if (txtRemarks.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblRemarksValidation, "Please enter your remarks");
            
        }
        return isValid;
    }
    
    // Common function to hide show validation labels
    private void hideShowValidation(JLabel lbl, String errorMessage) {
        if (errorMessage.isEmpty()) {
            lbl.setVisible(false);
        } else {
            lbl.setText(errorMessage);
            lbl.setVisible(true);
        }
        
    }

