            issues.add(txtAddVisitReason.getText());
            DefaultTableModel model = (DefaultTableModel) tblIssues.getModel();
            Object row[] = new Object[1];
            row[0] = txtAddVisitReason.getText();
            model.addRow(row);
            txtAddVisitReason.setText("");
